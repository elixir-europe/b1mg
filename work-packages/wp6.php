        <h2 class="has-text-grey-dark">Coordination Office: project management, communication, governance and sustainability</h2>
        <p class="mt-6">The goals of this WP are to implement the B1MG management structure and to oversee the project execution, ensuring effective and efficient coordination to deliver the project goals and expected impact within time, scope and budget.</p>
        <p> The B1MG management structure purposely aligns with the 1+MG initiative establishing the basis for an effective and efficient management of the initiative in the long term. In addition, we aim to define long term sustainability models for the components of the framework as well as for the actual implementation. Guidelines and recommendations on how to establish national mirror groups will be produced to promote uptake of the different recommendations at the national level.</p>
        <h2 class="mb-2 mt-55">Objectives</h2>
        <ul class="mt-2 xbp">
          <li><strong>O6.1:</strong> Establish the project governance structure mobilising the project resources and developing the project guidance (Task 6.1).</li>
          <li><strong>O6.2:</strong> Efficient and effective project monitoring collecting KPIs and tracking risk and opportunities to assist the project boards on taking informed decisions at all levels (Task 6.2).</li>
          <li><strong>O6.3:</strong> Develop and update the project data management plan along the project life cycle (Task 6.3).</li>
          <li><strong>O6.4:</strong> Develop and monitor the consortium communication plan that will facilitate internal and external communication (Task 6.4).</li>
          <li><strong>O6.5:</strong> Facilitate the set up and operation of national mirror groups through guidelines and best practice identification (Task 6.5).</li>
          <li><strong>O6.6:</strong> Develop and foster the adoption of a sustainability plan for the implementation of B1MG recommendations, identifying potential investments needs (Task 6.6).</li>
        </ul>
        <h2 class="mb-2 mt-55">Tasks</h2>
        <button class="accordion single-line"><strong>Task 6.1:</strong> Establishment of project management boards. </button>
        <div class="accordion-panel">
          <div>
            <p>Project managing boards will be mobilised early in the project and will be provided with the project handbook. The project handbook will follow <a href="https://ec.europa.eu/isa2/solutions/open-pm2_en">Open PM2 best practices</a> to provide the board members and the project participants with a clear definition of their roles and responsibilities. It will also outline the processes and assets the project will use to ensure contractual commitments in the Grant Agreement are delivered on time, within scope and budget, and with the expected level of quality.</p>
            <p> Monitoring activities will also be included in all meetings from impact KPIs to technical delivery metrics (deliverables and milestones), quality assurance, risks, opportunities and innovation management (identification of assets).</p>
            <p><strong class="has-text-danger">Participants:</strong>  ELIXIR Hub (Hannah Hurst, Nikki Coutts, Juan Arenas), all participants via project boards.</p>
            <p><strong class="has-text-danger">Participants' roles:</strong> ELIXIR Hub: develop the project handbook based on the Open PM2 best practice and experience in leading large infrastructure projects.</p>
          </div>
        </div>
        <button class="accordion single-line"><strong>Task 6.2:</strong> Project monitoring and support.</button>
        <div class="accordion-panel">
          <div>
             <p>This task monitors and controls activities to be carried out during the life time of the project in order to ensure project goals, benefits and impact are delivered. All monitoring and control activities will be carried out as defined in the project handbook, providing project boards with the required data to make informed decisions along project execution.</p>
             <p> Monitoring and control activities will require inputs from all participants and will be reported at the participant, work package and project level as defined in the project handbook, every six months.</p>
            <p><strong class="has-text-danger">Participants:</strong>  ELIXIR Hub (Hannah Hurst, Nikki Coutts, Juan Arenas), all participants via monitoring and support activities.</p>
            <p><strong class="has-text-danger">Participants' roles:</strong> ELIXIR Hub: coordinate the monitoring and control activities and generate an updated version of the project handbook to reflect the evolution of the project.</p>
          </div>
        </div>
        <button class="accordion single-line"><strong>Task 6.3:</strong> Project data management plan and correlated activities.</button>
        <div class="accordion-panel">
          <div>
             <p>The data management plan (DMP) will be delivered early in the project according to the project scope and the EC requirement, and will be focus on the management and organisation of project data. </p>
             <p>The project does not anticipate access to research data as the focus is in producing recommendations and guidelines for the implementation of a secure cross-border infrastructure.</p>
            <p><strong class="has-text-danger">Participants:</strong>  ELIXIR Hub (Hannah Hurst, Juan Arenas), all participants.</p>
            <p><strong class="has-text-danger">Participants' roles:</strong> ELIXIR Hub: develop the project data management plan and update it regularly along the project execution.</p>
          </div>
        </div>
        <button class="accordion single-line"><strong>Task 6.4:</strong> Development and implementation of communication and dissemination strategy.</button>
        <div class="accordion-panel">
          <div>
            <p>In this task we will develop the communication plan, including the development of the tools (i.e. website, newsletter, social media presence through Twitter and LinkedIn) communications guidance and assets (project logos, templates, style guides).</p>
            <p>Those resources will then be used by the beneficiaries and participants in all their project communications. A key focus will be to utilise the existing communications channels of partner organisations, ensuring outputs from the project reach a broad range of stakeholders.</p>
            <p>These activities will span the duration of the project: the early phase of the project will see the creation of the communications tools, with work to use these channels carried out through the whole life cycle of the project. Regular reviews of the effectiveness of the communications channels will be carried out during the project.</p>
            <p><strong class="has-text-danger">Participants:</strong> ELIXIR Hub (Andrew Smith, Erin Haskell, Martin Cook)</p>
            <p><strong class="has-text-danger">Participants' roles:</strong> ELIXIR Hub: develop the project communication plan monitoring its implementation to ensure it accomplish it the defined goals.</p>
          </div>
        </div>
        <button class="accordion single-line"><strong>Task 6.5:</strong> Guiding principles for mirror groups.</button>
        <div class="accordion-panel">
          <div>
            <p>Mirror groups are a national reflection of the working groups set up by the 1+MG Initiative. The idea is that they nurture the work of the main working groups and translate the goals of the Declaration into each country. Due to the differences among countries, both in terms of research and innovation and health systems governance and in terms of approaches with respect to the 1+MG initiative, mirror groups are set up in different configurations.</p>
            <p>This task will enable a mutual learning exercise and will help to fine tune the work of the different mirror groups. Moreover, it will help observer countries and those who have not yet joined the initiative to see how to step into the initiative in a structured and effective way or to set up their own structure.</p>
            <p>To this aim, we will first analyse the different existing mirror groups, and assess its structure, composition, governance and operating modes. We will survey mirror group components on the benefits/obstacles encountered and compare against the work delivered by the related working group. Finally, we will take advantage of country visits and workshops organised in WP5, together with the 1M Genome working groups meetings – in particular WG1 - to promote a mutual learning exercise between those individuals invited to participate.</p>
            <p>With this feedback, we will set up some guiding principles and identify best practices for the good set-up, running and coordination of mirror groups. </p>
            <p><strong class="has-text-danger">Participants:</strong> SCIII (Esther Rodriguez), ELIXIR Hub (Susanna Repo, Juan Arenas)</p>
            <p><strong class="has-text-danger">Participants' roles:</strong> ISCIII: perform the analysis of existing mirror groups, organise sessions of mutual learning back to back to Governing Board/WG1 meetings and develop a guide for mirror groups. ELIXIR Hub: contribute with their expertise on coordinating a network of national Life Science Nodes across Europe.</p>
          </div>
        </div>
        <button class="accordion single-line"><strong>Task 6.6:</strong> Defining a long-term sustainability model for the delivery of PM cross-border. </button>
        <div class="accordion-panel">
          <div>
            <p>Long-term sustainability of the 1+MG initiative will require an appropriate organisational structure and plan. The Governing Board of B1MG, formed by representatives designated by the signing and observer countries (as well as countries not currently involved in the initiative) will be crucial to this aim.</p>
            <p>Through this task and with the B1MG Governing Board, potential legal and financial frameworks for developing the initiative at international level will be explored together with its insertion/ institution as a major contributor to future initiatives (i.e. Partnership for Personalised Medicine, Mission on Cancer).</p>
            <p>The task will define the structure of a sustainability plan central to the future development of the initiative. This plan will be developed with the participation of the organisations involved in Task 5.4 plus those required to assess the structural aspects of sustainability, i.e. financial and operational ones as well as scientific excellence and leadership. It will be presented to the B1MG Governing Board.</p>
            <p> It will identify potential funding needs – both at national and European level according to different scenarios for the following years as well as enabling flexibility to enlarge the initiative. The sustainability model will analyse complementary existing initiatives and strategic partnerships to maximise the impact of the project. In this frame, and back to back with WP5, policy briefs will be prepared to inform relevant authorities and other stakeholders about the progress, enlargement, sustainability and future development plans of the 1+MG initiative. </p>
            <p><strong class="has-text-danger">Participants:</strong> ISCIII (Esther Rodriguez), ELIXIR Hub (Andrew Smith, Juan Arenas)</p>
            <p><strong class="has-text-danger">Participants' roles:</strong> ISCIII: develop and update policy briefs. The last policy brief will include a report on sustainability design and funding routes. ELIXIR Hub: contribute with their expertise on bringing together global funders and on addressing sustainability issues in distributed infrastructure.</p>
          </div>
        </div>

