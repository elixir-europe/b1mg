                <dt id="Multidisciplinary teams">Multidisciplinary teams</dt>
                <dd>Teams comprised of individuals who span across different areas of expertise to cover all knowledge
                    areas required for genomic medicine.</dd>

                <dt id="No-cost access plan">No-cost access plan</dt>
