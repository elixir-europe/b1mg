                    regulate appropriate and acceptable behaviour. E.g. legislation, policies, professional regulations,
                    codes of conduct.</dd>

                <dt id="Personal data">Personal data</dt>
                <dd>Data related to a living individual, who is likely to be identified by the data directly or combined
