            <dl>
                <dt id="Acceptance">Acceptance</dt>
                <dd>Perceived usefulness of genomic medicine to patients. Recognition from citizens, patients and
                    patients' associations of a positive impact of the use of genomic medicine on patients levels of
                    satisfaction</dd>

                <dt id="API">API</dt>
                <dd>Application Programming Interface. A software intermediary that allows two applications to talk to
                    each other.</dd>

                <dt id="Awareness">Awareness</dt>
                <dd>Public's level of understanding about the importance and implications of genomic medicine.</dd>

                <dt>Centrally</dt>
                <dd>Based within a national or regional node.</dd>

                <dt id="Clinical interpretation of genomic results">Clinical interpretation of genomic results</dt>
                <dd>Translation of the technical output of a clinical genetic or genomic test into potentially
                    clinically actionable information.</dd>

                <dt id="Cost-effectiveness assessment">Cost-effectiveness assessment</dt>
                <dd>Cost-effectiveness analysis is a form of economic analysis that compares the relative costs and
                    outcomes of different courses of action.</dd>

                <dt id="Costed implementation plan">Costed implementation plan</dt>
                <dd>A multi-year roadmap that enables governments to prioritise interventions, engage stakeholders
                    around one strategy, forecast costs and mobilise resources to meet identified gaps, namely to
                    implement genomics in healthcare systems.

                </dd>
                <dt id="Data protection">Data protection</dt>
                <dd>Certainity that personal data is used fairly, lawfully and transparently  for specified, explicit
                    purposes  in a way that is adequate, relevant and limited to only what is necessary, accurate and,
                    where necessary, kept up-to-date, for no longer than is necessary, and handled in a way that ensures
                    appropriate security, including protection against unlawful or unauthorised processing, access,
                    loss, destruction or damage.</dd>

                <dt id="Data reuse">Data reuse</dt>
                <dd>Reuse, or secondary use, of health data for purposes other than the primary reason for which they
                    were originally saved. Other purposes may include scientific research, development and innovation
                    activities, teaching and statistics.</dd>

                <dt id="Data reception">Data reception</dt>
                <dd>Uniform processes (such as quality control and standardisation) to receive (download) or access
                    (through API) both data and metadata in a consistent way, enabling infrastructures to adhere to
                    global standards and principles for genotypic and phenotypic data. It includes logically describing
                    datasets to an extent that they can become actionable on the infrastructure, even if they are stored
                    nationally or locally. (Adapted from the 1+MG Scoping paper)</dd>

                <dt id="Dataset structure">Dataset structure</dt>
                <dd>The dataset is formatted in a standard way to support interoperability, i.e. via use of
                    international standards.</dd>

                <dt id="Dedicated Governance">Dedicated governance</dt>
                <dd>The process by which decisions are made and implemented. Governance is the process by which public
                    institutions conduct public affairs and manage public resources.</dd>

                <dt id="Economic model">Economic model</dt>
                <dd>A structured approach to help decision-makers choose between alternative ways of using resources, by
                    weighting the cost of an action against the benefits that it provides. It is frequently used to
                    anticipate the costs and benefits of new health care technologies, policies and regulations.</dd>

                <dt id="Federated model">Federated model</dt>
                <dd>A distributed network of repositories for sharing genomic information.</dd>
                <dt id="Further processing">Further processing</dt>
                <dd>The processing of personal data for a different purpose(s) than the initially collected.</dd>

                <dt id="Genetic data">Genetic data</dt>
                <dd>Personal data related to the inherited or acquired genetic characteristics of an individual, which
                    give unique information about his/her physiology or health, that result from an analysis of a
                    biological sample from the individual in question. [ref. Art. 4(13) GDPR]</dd>

                <dt id="Guidelines for clinical interpretation of genomic results">Guidelines for clinical
                    interpretation of genomic results</dt>
                <dd>Guidelines for translating the technical output of a genetic or genomic test into potentially
                    clinically actionable information.</dd>

                <dt id="Guidelines for clinical reporting of genomic results">Guidelines for clinical reporting of
                    genomic results</dt>
                <dd>Guidelines for reporting the actionable results of a genetic or genomic test to the attending
                    clinician and/or patient.</dd>

                <dt id="Health data">Health data</dt>
                <dd>Personal data related to the physical or mental health of an individual independent of its origin
                    (e.g. healthcare context, research, clinical trials, the data subject directly, smart devices).
                    [ref. Art. 4 GDPR]</dd>

                <dt id="HTA">HTA framework</dt>
                <dd>Health Technology Assessment framework. A multidisciplinary process that uses explicit methods to
                    determine the value of health technology at different points in its lifecycle to help
                    decision-makers make informed decisions.</dd>

                <dt id="ICT">ICT (clinical) tools</dt>
                <dd>Information and communication technology, such as electronic health records, telehealth or online
                    resources.</dd>

                <dt id="ISO">ISO</dt>
                <dd>The International Organisation for Standardisation</dd>

                <dt id="Locally">Locally</dt>
                <dd>Within a single institution, i.e. not beyond a lab, department or hospital.</dd>

                <dt id="Metadata">Metadata</dt>
                <dd>Data that provides information about other data. For example, the origin of the data, the processing
