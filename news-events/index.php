<h3 class="has-text-danger is-size-4">Professor emeritus Gert-Jan van Ommen (1947-2020)</h3>
<figure class="is-pulled-right ml-5">
  <img src="/images/news/gert-jan-van-ommen.jpg" alt="Gert-Jan van Ommen" width="300" class="border-grey"/></a>
</figure>
<p>With great sadness we learned that our admired colleague Gert-Jan van Ommen passed away last weekend, Saturday 7 November. </p>
<p>As Professor in Human Genetics and head of the Department of Human Genetics of Leiden University Medical Center (LUMC) from 1992-2012, Gert-Jan has greatly contributed to the Dutch and international human genetics and genomics field. He was driven by research into the biology of rare diseases, employing the most advanced technologies in research and therapy development. He inspired many international colleagues and young researchers to improve healthcare and make scientific discoveries work for patients. </p>

<p>Gert-Jan had a great interest in the ethical, legal and societal aspects of genome science, and in public and professional communication. He was Editor-in-chief of the European Journal of Human Genetics, has been president of the Human Genome Organisation (1998-2000) and of the Dutch (1993-2000) and European (2003-2004) Societies of Human Genetics. Beyond his personal scientific work, Gert-Jan was particularly involved in development of the national and international research infrastructure in the human genetics field, such as in the field of biobanking. Gert-Jan had a great ability to bring together people with opposing viewpoints, and he was a ‘mentor’ to many colleagues.</p>

<p>As a strong advocate for sharing and using genetic information, Gert-Jan has been one of the driving forces behind the Dutch participation in the European 1+ Million Genomes initiative. Despite his retirement and severe disease, he has been highly involved in the 1+MG initiative in several ways. Even until just a few weeks ago he was an active member of the 1+MG Coordination Group. Until the very end he remained committed to the 1+MG goals and process to foster European collaboration in the implementation of genome-based health. </p>

<p>We have lost a great, amiable man who always kept up the good spirit in conversations through a catchy phrase. We will miss him dearly.</p>

<p>We would like to extend our sincerest sympathy to his wife, friends and colleagues.</p>
