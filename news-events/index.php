        <h3 class="has-text-danger is-size-4">Five project deliverables published </h3>
        <p>Five deliverables from the project have just been published:</p>
        <ul>
          <li class="pb-2"><strong><a href="https://zenodo.org/record/4813451#.YK_dVpNKj0o">Generation of a stakeholder portal within the project webpage containing the information resulting from the activities of the SCG</a>  </strong>(D1.2). The stakeholder portal allows stakeholders in the B1MG project to share documents and collaborate to provide feedback on the project. See the <a href="/work-packages/wp1">Work Package 1 page</a> for background information.</li>
          <li class="pb-2"><strong><a href="https://zenodo.org/record/4889391#.YLYFWzZKhQY">Quality metrics for sequencing</a> </strong>(D3.1). This establishes quality metrics for the generation of whole genome sequencing (WGS) and whole exome sequencing (WES) data. </li>
          <li class="pb-2"><strong><a href="https://zenodo.org/record/4819149#.YK_bKZNKj0o">Documented best practices in sharing and linking phenotypic and genetic data - 1v0</a> </strong>(D3.7). This document describes best practices in sharing and linking phenotypic and genetic data in both the healthcare sector and in research. See the <a href="/work-packages/wp3">Work Package 3 page</a> for background information.</li>
          <li class="pb-2"><strong><a href="https://zenodo.org/record/4889401#.YLYHHzZKhQY">Secure cross-border data access roadmap - 1v0</a> </strong>(D4.1). A live document with plans for the infrastructure to provide secure cross-border data access to genetic and phenotypic data. See the <a href="/work-packages/wp4">Work Package 4 page</a> for background information.</li>
          <li class="pb-2"><strong><a href="https://zenodo.org/record/4813544#.YK_dbZNKj0o">Analysis and assessment of mirror groups</a> </strong>(D6.6). The National Mirror Groups are groups within a country who are developing the local capacity to integrate with the B1MG data sharing network. Mirror groups have different configurations in different countries, and may not even exist yet in some countries. This deliverable investigates how these mirror groups can be aligned. See the <a href="/work-packages/wp6">Work Package 6 page</a> for background information.</li>
        </ul>

