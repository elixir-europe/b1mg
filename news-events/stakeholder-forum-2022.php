                    <p><strong>3. Key updates on 1+MG/B1MG</strong></p>
                    <p>During this session we will share the key outcomes of the 1+MG/B1MG project that are relevant to the theme of the day:</p>

                    <ul>
                        <li><strong>Trust Framework:</strong> Serena Scollen (ELIXIR Hub)<br>
                            Key updates will be presented from 1+MG/ the B1MG project. This session will include a brief overview of the 1+MG Trust Framework that was the focus of the previous stakeholder forum, with updates on its 3 components - Data governance, data standards and quality, and technical infrastructure.
                        </li>
                        <li><strong>Maturity Level Model (MLM):</strong> Astrid Vicente, Instituto Nacional de Saúde Dr. Ricardo Jorge (INSA)
