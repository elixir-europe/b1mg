                    <p>The Dutch Techcentre for Lifesciences (DTL)</p>
                    <p>Dr. Ruben Kok received his PhD in microbial genetics in 1995 at the University of Amsterdam and followed his scientific career at Yale University, Gist Brocades (now DSM) and Wageningen University and Research. In 2005, dr. Kok was appointed director of the Netherlands Bioinformatics Centre (NBIC). 9 Years later, with several colleagues in the field, Ruben Kok co-founded DTL, the Dutch Techcentre for Lifesciences and became its first director. Meanwhile DTL connects over 40 public and private organisations in a national digital life sciences platform, supporting strategies towards data stewardship, data analytics and data sharing in molecular life sciences.
