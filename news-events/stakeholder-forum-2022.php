                    <p>The Beyond 1 Million Genomes (B1MG) project aims to make it easier to share genomics data across borders within Europe. It supports the EU's 1+ Million Genomes Initiative (1+MG) by bringing together diverse stakeholders from scientific and technical communities, international initiatives, projects, national healthcare systems, as well as industry to collectively create the infrastructure, the legal guidance and the best practices to enable this access. To facilitate this process, the efforts of 1+MG/B1MG have also included increasing the engagement, awareness and alignment of the different stakeholders involved in the genomics-based research and healthcare landscape within Europe.</p>
                    <p>Being the last stakeholder forum organised by the B1MG project, this meeting will centre on identifying how stakeholders could contribute to the scale up and sustainability of the B1MG efforts.</p>

                    <p>Along with updates on progress to date (the 1+MG Trust Framework, the Maturity Level Model and industry engagement), highlights of the the day will include a panel discussion on industry engagement and parallel workshops to consider scale up and sustainability in the context of rare disease, cancer, common complex disease and infectious disease.</p>

