<h2 class="mt-6 is-size-3">The role of the European Commission (EC)</h2>
<p><strong>Note:</strong> This involves the EC's Directorate-General for Communications Networks, Content and Technology (DG-Connect), Directorate-General for Research and Innovation (DG-RTD) and Directorate-General for Health and Food Safety (DG-SANTE).</p>
<p>The 1+MG is a strategically important initiative for the EC to shape Europe’s healthcare and digital future. The EC is the custodian of the 1+MG Declaration, co-drafting, promoting, facilitating implementation and supporting efforts to get new countries to sign.</p>
<p>The EC will link 1+MG with other initiatives implementing the relevant EU priorities (digital agenda, data spaces, AI, cancer beating plan), with ongoing H2020 projects and with future funding opportunities.</P>

<h3 class="mb-0">The Role of the EC team in 1+MG</h3>
<ul class="mt-2">
  <li>Facilitation of communication with Member State representatives in the framework of the regular 1+MG Signatory meetings.
    <ul>
      <li>Advise and facilitate actions of the 1+MG coordination group.</li>
      <li>Provide facilities for physical Signatory and Working Group meetings when they are held in Brussels.</li>
      <li>Run surveys and written consultations of 1+MG signatory states on behalf of the 1+MG coordination group.</li>
      <li>Facilitate interaction with external stakeholders.</li>
    </ul>
  </li>
  <li>Chairing of Signatory meetings and providing Secretariat for them, until a final governance of the 1+MG initiative has been developed and implemented. Preparation for these meetings is in conjunction with the 1+MG coordination group, part of WG1.</li>
</ul>
<h3 class="mb-0">The Role of the EC team in B1MG</h3>
<ul class="mt-2">
  <li>The  EC is the funding authority for the B1MG project that has a contractual relationship with the B1MG consortium.</li>
  <li>It monitors the  progress and spending in the project. The EC Project Officer follows the project closely, organises periodic project reviews and ultimately approves its deliverables and outcomes (supported by the assessment by independent experts).</li>
</ul>

