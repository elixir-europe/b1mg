      <p class="mt-6">This page gives an overview of how the B1MG project, the 1+MG Initiative, the European Commission and stakeholders work together. It explains the support the B1MG project gives to the 1+MG Initiative.</p>
        <h2 class="mt-55 is-size-3">The role of 1+MG</h2>
        <p>The <a href="https://ec.europa.eu/digital-single-market/en/european-1-million-genomes-initiative">1+ Million Genomes Initiative (1+MG)</a> is a commitment of 23 European countries to give cross-border access to one million sequenced genomes by 2022.</p>
        <ul>
          <li>1+MG will drive the development of a European infrastructure for federated and secure cross-border access to genomic and personalised medicine data. It will set up the collaboration of signatory countries to:
        <ul>
          <li>Ensure that appropriate technical infrastructure is available all over the EU. This infrastructure will allow secure, federated access to genomic data. The access will be based on common standards and support applicable regulations.</li>
          <li>Ensure that ethical and legal issues concerning genomics data are fully addressed by the technical infrastructure. These issues include the protection of personal data, security of stored data, ethical use of data, and clear data ownership rules.</li>
          <li>Ensure that the general public and policy makers in Member States and signatory countries are informed about genomics and genomics-based health. This will ensure its uptake by healthcare systems and integration into personalised healthcare and prevention.</li>
        </ul>
      </li>
        <li>1+MG is responsible for the relationships with signatory countries and the European Commission (EC), alignment among <strong>National Mirror Groups (NMGs)</strong> and the 10x 1+MG Working Groups. This includes their activities and long term strategy.</li>
        <li>1+MG Signatory countries will be represented on the B1MG Governing Board (see <a href="/about/how-organised">How the project is organised</a>).</li>
      </ul>
      <h2 class="mt-6 is-size-3">The role of the B1MG project</h2>
      <p>The B1MG project primary role is to support the implementation of the 1+MG initiative by delivering the B1MG <strong>Description of Action (DoA)</strong> (project work plan).  Amendments to the DoA will be sought to ensure the DoA remains aligned with 1+MG Roadmap. The B1MG Work Package structure ensures that 1+MG Working Groups, Stakeholders and National Mirror Group needs are taken into consideration via the participation in the WPs while implementing the DoA.</p>
      <p>The B1MG will co-locate meetings with the 1+MG Coordination Team to align 1+MG and B1MG activities at the strategic and operational levels.</p>
      <p>As B1MG project is a Coordination and Support Action (CSA) project there will be no (or limited) Research and Innovation (RIA) driven activity. It is a time limited project (June 2020 to October 2023).</p>
