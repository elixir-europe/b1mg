        <p>The B1MG project has three main objectives. For an overview of the Work Packages (WPs) mentioned here, see the <a href="/work-packages">Work Packages section</a>.</p>
        <h2 class="is-size-4">Objective 1: To work with regional, national and European stakeholders to define the requirements for cross-border access to genomics and personalised medicine data.</h2>
            <p>To define the requirements for cross-border access to genomics and personalised medicine data, B1MG will establish a <strong>Stakeholder Coordination Group (SGC)</strong> to gather input at regular intervals from all relevant stakeholders. These requirements will be fed into Objectives 2 and 3. The group will include national-level representatives from the 1+MG initiative and beyond, experts from the public and private sectors, policy-makers, and patients and citizens more generally. </p>
            <p>Using personalised medicine Use Cases, we will outline the benefits that can be derived for patients. At least one open event - the ‘B1MG Summit’ - will be organised (in addition to the annual general assembly meeting), to ensure that all stakeholders have an opportunity to contribute and be informed about B1MG progress.</p>
