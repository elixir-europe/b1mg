        <p>The project tasks are divided into six units of work, or <strong>Work Packages (WPs)</strong>. Each Work Package has a team assigned to it, and each deals with a different aspect of the project. The Work Package personnel are drawn from the
            <a
                href="/about/participants">28 participating organisations</a> in the project.</p>
        <p>The Work Packages are guided by the <strong>Project Coordinator</strong> plus <strong>six management and governance bodies</strong>.</p>
        <figure class="img-full-width mt-55"><img src="/images/b1mg-governance-chart.svg" alt="Governance chart" /></figure>

        <h2 class="is-size-3 mt-6">Management and governance</h2>
        <h3 class="is-size-5 mb-2 mt-5">Project Coordinator</h3>
        <p class="mt-0">Serena Scollen (Head of Human Genomics and Translational data, ELIXIR ). The Coordinator is responsible for the overall management and reporting of the project.</p>
        <h3 class="is-size-5 mb-2">Governing Board</h3>
        <p class="mt-0">The B1MG Governing Board is composed of members from 1+MG signatory and observer countries. It is the final decision making body of the B1MG project and will provide strategic guidance. It meets every six months, and when feasible the meetings are collocated with 1+MG signatories. Board members can propose additional members (countries) at any time during the project. The Coordination Office (see below) seeks the approval of the Board for any significant modifications to the project Grant Agreement.<br /><i class="icofont-arrow-right has-text-primary"></i> <a href="governing-board">Governing Board page</a></p>
        <h3 class="is-size-5 mb-2">Coordination Office</h3>
        <p class="mt-0">This team is responsible for supporting and monitoring the execution of the project work plan, and for managing the project budget. They  provide direct support to the 1+MG initiative by organising the 1+MG Coordination team meetings, which are also chaired by the B1MG Coordinator. The 1+MG Coordination team oversee the implementation of the 1+MG Roadmap, which will enable 1+ Million Genomes to be accessible across Europe by 2022.  </p>

        <h3 class="is-size-5 mb-2">Operational Group</h3>
        <p class="mt-0">This group is chaired by the Project Coordinator. It brings together WP leaders and 1+MG Working Group leaders to monitor the project progress. The group reports and make recommendations to the Governing Board (directly or via the Coordination Office) on decisions that have a significant impact for the B1MG project. The Operational Group takes some decisions about the direction of the project, but significant changes are referred to the Board. <br /><i class="icofont-arrow-right has-text-primary"></i> <a href="operational-group">Operational Group page</a></p>

        <h3 class="is-size-5 mb-2">General Assembly</h3>
        <p>The General Assembly is composed of people from all the institutes involved in the project. The Assembly meets at least once a year to ensure everyone involved in the project can contribute to the project outcomes. Any member of the Assembly can make a request via their WP leader or the Coordination Office, who will discuss the matter with the Operational Group. The Operational Group can then raise the issue with the Board, if required.</p>
        <h3 class="is-size-5 mb-2">Scientific and Ethics Advisory Board (SEAB)</h3>
        <p>The SEAB is composed of international experts not directly involved in the B1MG project. They meet once a year at the General Assembly, and virtually when required. They advise the Governing Board. <br /><i class="icofont-arrow-right has-text-primary"></i> <a href="seab">SEAB page</a></p>
        <h3 class="is-size-5 mb-2">National Mirror Groups</h3>
        <p>The National Mirror Groups (NMG) bring together policy makers, and scientific and technical experts in each country that is involved in the 1+MG Roadmap and who participate in the 1+MG Working Groups.   These are groups within a country who are developing the local capacity to integrate with the B1MG Europe-wide data sharing network. For example, they are identifying national legal issues concerning the re-use of genomics data outside their own country.</p>
        <p>The NMGs are crucial to the adoption at the national level of the B1MG outcomes, and the B1MG project is developing guidelines to assist the implementation and long term operation of NMGs. Contact: <a href="mailto:b1mg-nmg-coordinators@elixir-europe.org">b1mg-nmg-coordinators@elixir-europe.org</a>.</p>

