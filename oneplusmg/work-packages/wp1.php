        <h2 class="has-text-grey-dark">Framework for cooperation through stakeholders engagement, awareness and alignment</h2>

<div class="card px-3 py-3 mt-55 grey announcement has-text-weight-bold"> The Stakeholder Portal is now live - see the <a href="https://b1mg-project.eu/news-events/index.php#stakeholder-portal">news story</a> or go to the <a href="https://docs.google.com/forms/d/e/1FAIpQLSfDRtFrsYIIkOjz0343Rub4F-74foqt8MZJFXsFg1iVyMGc2w/viewform">registration form</a>.</div>
        <p class="mt-5">Genomics-based healthcare requires stakeholders to take on a new, active role in decisions about their health and the health of the whole society. Consultation and buy-in are central to enable them to take this active role.</p>
        <p>This WP will implement a way to drive stakeholder involvement, taking into account the need to coordinate between scientific and technical communities, as well as national healthcare systems. A particular concern is citizen engagement. Access to personalised medicine data ultimately rests on the trust of patients and the public more broadly. In particular, the B1MG summit will bring together all internal (to the project) and external stakeholders in an open event. This will facilitate wider consultation and increase public trust.</p>
        <p>All of the stakeholder engagement tools will be designed and presented so as to encourage participation from under-represented groups. </p>
        <h2 class="mb-2 mt-55">Objectives</h2>
        <ul class="mt-2 xbp">
          <li><strong>O1.1:</strong> Create a Stakeholder Coordination Group (SCG) to engage and align a broad range of European and global stakeholders. (Task 1.1).</li>
          <li><strong>O1.2:</strong> Support the creation of a cross-European framework to align National Mirror Groups, including thematic stakeholders to facilitate co-creation and literacy, as well as alignment on best practices, so as to ensure synergistic implementation in national healthcare systems. (Task 1.2 & Task 1.3). Using this framework, WP1 will:</li>
          <li><strong>O1.3:</strong>  In collaboration with WP2, WP3, WP4 and WP5 drive a process to allow connection of national infrastructures and legal agreements to enable cross-border data access.</li>
          <li><strong>O1.4:</strong>   Provide operational tools and feedback to the Member States, based on the wider perspective of the SCG, including inputs from global organisations working on implementational strategies for genomic medicine and Personalised Medicine, contributing to a global co-creation process.</li>
        </ul>
        <h2 class="mb-2 mt-55">Tasks</h2>
        <button class="accordion"><strong>Task 1.1:</strong> Creating a <strong>Stakeholder Coordination Group (SCG)</strong> with framework documents for effective implementation of an infrastructure for genomics based health and its implementation into healthcare within the EU.</button>
        <div class="accordion-panel">
          <div>
            <p>WP1, on behalf of the B1MG consortium, will invite representatives from all major and relevant stakeholder groups (academia, policy-makers, health care providers, research infrastructures, industry, and citizens representatives, among others) to co-create a stable framework to systematically tackle the challenges defined by the project.</p>
            <p>The stakeholders, with the national mirror groups/WP5, will tackle the issues in three distinct areas, creating a space of dialogue for building an agenda that will include:</p>
            <ol><li>A decision making framework for public-private involvement and increased literacy in personalised health amongst the stakeholders;
              <li>A framework for future proofing the area to facilitate emerging health technologies integration into the healthcare systems and
              <li>identifying future mechanisms of cooperation between stakeholders within and outside the EU. </li>
            </ol>
            <p>Through the development of a problem statement and the framework document for each thematic area, the Stakeholder Coordination Group (SCG) will generate recommendations based on the assessment of their own integration processes, as well as monitoring multimodal genomic medicine programmes outside Europe to increase interactions with a global Personalised Medicine community. </p>
            <p>Close attention will be paid to key projects and programmes outside of Europe, such as the MASTER KEY project from Japan, the Qatar Precision Medicine National Strategy, Genome Canada, Therapeutic Innovation Australia and the US-based Personalised Medicine Coalition. Experts from these initiatives will be invited to participate in expert panels. </p>
            <p><strong class="has-text-danger">Participants:</strong> EAPM (Denis Horgan), EMBL (Jan Korbel), DTL-Projects (Ruben Kok), EATRIS (Toni Andreu).
            <p><strong class="has-text-danger">Participants' roles:</strong> EAPM to establish the Stakeholder Coordination Group and establish the terms of reference for its operation. Linked to the 1+MG working groups, EMBL and EAPM will coordinate the development of the problem statement and framework documents. DTL-PROJECTS to act as the link with the EU Member States, as well as the mirror groups, to ensure alignment. EATRIS to act as the link with other infrastructures, such as ELIXIR, BBMRI, ECRIN and non-EU initiatives.</p>
          </div>
        </div>
        <button class="accordion"><strong>Task 1.2:</strong> Creating a stakeholder portal to efficiently deliver, to the operational WPs (2, 3, 4 and 5), a timely analysis of challenges, opportunities and solutions resulting from the activities of the SCG.</button>
        <div class="accordion-panel">
          <div>
            <p>The value chain for the use of genomic data into clinical decision making represents a strong operational challenge, not only for the integration and FAIRification processes at early stages during data generation, but during the entire translational value chain. This process is defined by a complex fabric of interconnectedness of research, healthcare, policy, reimbursement, industry and regulatory domains, whose success can only occur through structural and cross- sectional collaboration.</p>
            <p>In the specific case of the B1MG project, the definition of process becomes even more challenging due to the multinational nature of the initiative. Through this task, stakeholder perspectives will be brought to the technical WPs (2, 3, 4, and 5) in charge of creating the genomic translational pipeline.</p>
            <p> WP1 will create, within the B1MG domain and in close collaboration with WP6 (see Task 6.4), a stakeholder portal to deliver tools and define workflows to de-risk and improve the flow of data generation and integration towards patients. These tools will help reduce cross-border barriers, and facilitate the development of operational pipelines based on the integration of genomic and health data.</p>
            <p><strong class="has-text-danger">Participants:</strong> EATRIS (Toni Andreu, Anne-Charlotte Fauvel), DTL-Projects (Ruben Kok), EAPM (Denis Horgan), EMBL (Jan Korbel), ELIXIR Hub (Andrew Smith, Erin Haskell, Martin Cook via Task 6.4)). All partners via project boards. </p>
            <p><strong class="has-text-danger">Participants' roles:</strong> EATRIS to define the operational framework for the portal. DTL-PROJECTS to ensure that it is integrated into the operational WPs (1, 2, 3, and 4) workflows. EAPM and EMBL to ensure that the Stakeholder Coordination Group will provide inputs. ELIXIR Hub to align the stakeholder portal with the communication strategy defined in WP6.</p>
          </div>
        </div>
        <button class="accordion"><strong>Task 1.3:</strong> Disseminate, to the project, Use Cases (e.g. from Horizon Europeâ€™s 'Cancer Mission', and the <a href="https://www.ejprarediseases.org/">EJP RD project</a>) tools, processes and operations developed in WP2 to 5, to de-risk their own value chains.</button>
        <div class="accordion-panel">
          <div>
            <p>The Cancer Mission, foreseen by Horizon Europe and the development of the EJP RD, represent two major landmarks in the European Research Area, demonstrating the need for a digital research infrastructure for genomics-based health research and patient care. </p>
            <p>Both initiatives, as well as the proposed partnership on Personalised Medicine, define a full cross-border translational pipeline where the generation of high-quality FAIRified genomic data will be embedded into the health care decision-making process. </p>
            <p>To solve challenges related to the specific development of both initiatives, B1MG will generate a toolbox of solutions (WP2 to 5), including tools specific for these domains, optimising the process for data integration fully adapted to the Cancer and Rare Diseases research challenges.</p>
            <p><strong class="has-text-danger">Participants:</strong> DTL-Projects (Ruben Kok, GertJan B. van Ommen), EAPM (Denis Horgan), EATRIS (Toni Andreu, Anne-Charlotte Fauvel), EMBL (Jan Korbel). All partners via project boards.</p>
            <p><strong class="has-text-danger">Participants' roles:</strong> DTL-PROJECTS to deliver to the Use Cases the outcomes of the different WPs and to ensure alignment with the mirror groups. EATRIS to provide operational support to DTL-PROJECTS. EAPM and EMBL to ensure that the Stakeholder Coordination Group can provide inputs to the rare diseases and cancer domains.</p>
