      <p>The 28 participating organisations have been carefully selected to provide all the expertise needed for the the project. The <a href="https://ec.europa.eu/digital-single-market/en/european-1-million-genomes-initiative">1+MG Initiative</a> signatories will be the longer term funders and implementers, so there is a close alignment between B1MG and 1+MG participants (See LINK NEEDED   ). Almost all of the 1+MG coordination team are involved in the B1MG project, either as a Work Package leader or a Governing Board member. Many B1MG partners are involved in the 1+MG working groups. </p>
<p>At a country level, the B1MG consortium includes partners and Governing Board members beyond the 1+MG signatories. It remains open to involve additional EU Member States  and associated countries during the lifetime of the project.</p>

